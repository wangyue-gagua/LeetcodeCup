def plotSquares(squares: List[List[int]]):
    # 画出正方形
    for square in squares:
        x = square[0]
        y = square[1]
        l = square[2]
        plt.plot([x - l / 2, x + l / 2, x + l / 2, x - l / 2, x - l / 2], [y - l / 2, y - l / 2, y + l / 2, y + l / 2, y - l / 2], color='red', alpha=0.2)

    plt.show()

